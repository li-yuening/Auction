package edu.uibe.model;

public class ItemInfo {
	public String itemNumber;
	public String itemName;
	public String itemDescription;
	public String startBidDate;
	public String endBidDate;
	public String startBidPrice;
	public String itemClassNumber;
	public String itemBelongedNumber;
	
	public String getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(String itemNumber) {
		this.itemNumber = itemNumber;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemDescription() {
		return itemDescription;
	}
	public void setItemDescription(String itemDescription) {
		this.itemDescription = itemDescription;
	}
	public String getStartBidDate() {
		return startBidDate;
	}
	public void setStartBidDate(String startBidDate) {
		this.startBidDate = startBidDate;
	}
	public String getEndBidDate() {
		return endBidDate;
	}
	public void setEndBidDate(String endBidDate) {
		this.endBidDate = endBidDate;
	}
	public String getStartBidPrice() {
		return startBidPrice;
	}
	public void setStartBidPrice(String startBidPrice) {
		this.startBidPrice = startBidPrice;
	}
	public String getItemClassNumber() {
		return itemClassNumber;
	}
	public void setItemClassNumber(String itemClassNumber) {
		this.itemClassNumber = itemClassNumber;
	}
	public String getItemBelongedNumber() {
		return itemBelongedNumber;
	}
	public void setItemBelongedNumber(String itemBelongedNumber) {
		this.itemBelongedNumber = itemBelongedNumber;
	}
}